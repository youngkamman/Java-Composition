public class Sink {
    private String brand;
    private int waterpressure;

    public Sink(String brand, int waterpressure) {
        this.brand = brand;
        this.waterpressure = waterpressure;
    }

    public String getBrand() {
        return brand;
    }

    public int getWaterpressure() {
        return waterpressure;
    }
}
